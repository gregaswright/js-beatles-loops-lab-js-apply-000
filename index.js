function theBeatlesPlay(musicians, instruments) {
  let array = [];
  for (var i = 0; i < musicians.length; i++) {
    array.push(`${musicians[i]} plays ${instruments[i]}`)
  }
  return array
}

function johnLennonFacts(array){
  var i = 0;
  while (i < array.length) {
    return `${array}!!!`
    array++
  }
}
